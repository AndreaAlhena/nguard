<h1>Async Validators</h1>
<p>A set of asynchronous validators for server-side validation and external service integration.</p>

<h2 class="mt-4">Planned Validators</h2>
<p>The following validators are planned for v0.10.0 - Async Validators:</p>

<ul>
    <li>Unique - Validates uniqueness against server</li>
    <li>Exists - Validates existence in database</li>
    <li>UniqueExcept - Validates uniqueness except for specific value</li>
    <li>RemoteValidation - Custom server-side validation</li>
    <li>ReCaptcha - Google reCAPTCHA validation</li>
    <li>HCaptcha - hCaptcha validation</li>
    <li>Turnstile - Cloudflare Turnstile validation</li>
</ul>
