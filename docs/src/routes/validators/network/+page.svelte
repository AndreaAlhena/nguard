<h1>Network Validators</h1>
<p>A set of validators for network-related values like IP addresses and MAC addresses.</p>

<h2 class="mt-4">Planned Validators</h2>
<p>The following validators are planned for v0.2.0 - Enhanced Strings &amp; Network:</p>

<ul>
    <li>IP - Validates IPv4 or IPv6 address</li>
    <li>IPv4 - Validates IPv4 address specifically</li>
    <li>IPv6 - Validates IPv6 address specifically</li>
    <li>MacAddress - Validates MAC address format</li>
</ul>
