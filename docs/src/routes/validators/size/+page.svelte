<h1>Size Validators</h1>
<p>A set of validators for validating sizes of strings, arrays, and files.</p>

<h2 class="mt-4">Planned Validators</h2>
<p>The following validators are planned for v0.3.0 - Numeric Power:</p>

<ul>
    <li>Size - Validates exact size</li>
    <li>MinSize - Validates minimum size</li>
    <li>MaxSize - Validates maximum size</li>
    <li>SizeBetween - Validates size is within range</li>
</ul>
