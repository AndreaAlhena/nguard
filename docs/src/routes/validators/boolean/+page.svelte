<h1>Boolean Validators</h1>
<p>A set of validators for boolean and acceptance values.</p>

<h2 class="mt-4">Planned Validators</h2>
<p>The following validators are planned for v0.6.0 - Boolean &amp; Acceptance:</p>

<ul>
    <li>Boolean - Validates boolean value</li>
    <li>Accepted - Validates value is accepted (yes, on, 1, true)</li>
    <li>Declined - Validates value is declined (no, off, 0, false)</li>
    <li>AcceptedIf - Conditional acceptance</li>
    <li>DeclinedIf - Conditional decline</li>
    <li>Truthy - Validates truthy value</li>
    <li>Falsy - Validates falsy value</li>
</ul>
