<h1>Array Validators</h1>
<p>A set of validators for arrays and collections.</p>

<h2 class="mt-4">Planned Validators</h2>
<p>The following validators are planned for v0.7.0 - Arrays &amp; Collections:</p>

<ul>
    <li>Array - Validates value is an array</li>
    <li>ArrayMin - Validates minimum array size</li>
    <li>ArrayMax - Validates maximum array size</li>
    <li>ArrayBetween - Validates array size is within range</li>
    <li>Distinct - Validates array contains unique values</li>
    <li>InArray - Validates value exists in array</li>
    <li>Contains - Validates array contains specific value</li>
    <li>DoesntContain - Validates array doesn't contain specific value</li>
    <li>RequiredArrayKeys - Validates required keys exist in array</li>
    <li>ArrayOf - Validates array contains specific type</li>
</ul>
