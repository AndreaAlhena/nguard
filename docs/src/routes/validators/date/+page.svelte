<h1>Date Validators</h1>
<p>A comprehensive set of validators for date and time values.</p>

<h2 class="mt-4">Planned Validators</h2>
<p>The following validators are planned for v0.4.0 - Date &amp; Time:</p>

<ul>
    <li>Date - Validates date format</li>
    <li>DateFormat - Validates specific date format</li>
    <li>DateEquals - Validates date equals specific date</li>
    <li>After - Validates date is after specified date</li>
    <li>Before - Validates date is before specified date</li>
    <li>AfterOrEqual - Validates date is after or equal to specified date</li>
    <li>BeforeOrEqual - Validates date is before or equal to specified date</li>
    <li>AfterToday - Validates date is after today</li>
    <li>BeforeToday - Validates date is before today</li>
    <li>Today - Validates date is today</li>
    <li>Timezone - Validates timezone format</li>
    <li>Time - Validates time format</li>
    <li>DateRange - Validates date is within a range</li>
</ul>
