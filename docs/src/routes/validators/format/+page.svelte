<h1>Format Validators</h1>
<p>A set of validators for real-world data formats like credit cards, phone numbers, and more.</p>

<h2 class="mt-4">Planned Validators</h2>
<p>The following validators are planned for v0.9.0 - Real-World Formats:</p>

<ul>
    <li>CreditCard - Validates credit card number</li>
    <li>Phone - Validates phone number format</li>
    <li>PostalCode - Validates postal/zip code</li>
    <li>IBAN - Validates International Bank Account Number</li>
    <li>BIC - Validates Bank Identifier Code</li>
    <li>ISBN - Validates International Standard Book Number</li>
    <li>EAN - Validates European Article Number</li>
    <li>SSN - Validates Social Security Number</li>
    <li>VatNumber - Validates VAT number format</li>
</ul>
