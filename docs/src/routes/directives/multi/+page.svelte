<h1>Multi Validation Directives</h1>
<p>A set of directives for multi-field validation in templates.</p>

<h2 class="mt-4">Available Directives</h2>
<ul>
    <li><a href="/directives/multi/confirmed">Confirmed</a> - Validates field matches a confirmation field</li>
    <li><a href="/directives/multi/different">Different</a> - Validates field is different from another</li>
    <li><a href="/directives/multi/doesnt-end-with">DoesntEndWith</a> - Validates field doesn't end with values</li>
    <li><a href="/directives/multi/doesnt-start-with">DoesntStartWith</a> - Validates field doesn't start with values</li>
    <li><a href="/directives/multi/ends-with">EndsWith</a> - Validates field ends with one of values</li>
    <li><a href="/directives/multi/greater-than">GreaterThan</a> - Validates field is greater than another</li>
    <li><a href="/directives/multi/greater-than-or-equal">GreaterThanOrEqual</a> - Validates field is greater than or equal to another</li>
    <li><a href="/directives/multi/lesser-than">LesserThan</a> - Validates field is less than another</li>
    <li><a href="/directives/multi/lesser-than-or-equal">LesserThanOrEqual</a> - Validates field is less than or equal to another</li>
    <li><a href="/directives/multi/required-if">RequiredIf</a> - Validates field is required conditionally</li>
    <li><a href="/directives/multi/same">Same</a> - Validates field matches another</li>
    <li><a href="/directives/multi/starts-with">StartsWith</a> - Validates field starts with one of values</li>
</ul>