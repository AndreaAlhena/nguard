<h1>Number Directives</h1>
<p>A set of directives for numeric value validation in templates.</p>

<h2 class="mt-4">Available Directives</h2>
<ul>
    <li><a href="/directives/number/between">Between</a> - Validates that a number is within a specific range</li>
    <li><a href="/directives/number/integer">Integer</a> - Validates that a value is an integer</li>
    <li><a href="/directives/number/max">Max</a> - Validates that a number doesn't exceed a maximum</li>
    <li><a href="/directives/number/min">Min</a> - Validates that a number meets a minimum</li>
    <li><a href="/directives/number/negative">Negative</a> - Validates that a number is negative</li>
    <li><a href="/directives/number/numeric">Numeric</a> - Validates that a value is numeric</li>
    <li><a href="/directives/number/positive">Positive</a> - Validates that a number is positive</li>
</ul>
