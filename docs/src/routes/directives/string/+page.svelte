<h1>String Directives</h1>
<p>A set of directives for string value validation in templates.</p>

<h2 class="mt-4">Available Directives</h2>
<ul>
    <li><a href="/directives/string/alpha">Alpha</a> - Validates that a string contains only alphabetic characters</li>
    <li><a href="/directives/string/alpha-dash">Alpha Dash</a> - Validates that a string contains only alphanumeric characters, dashes, and underscores</li>
    <li><a href="/directives/string/alpha-num">Alpha Num</a> - Validates that a string contains only alphanumeric characters</li>
    <li><a href="/directives/string/ascii">ASCII</a> - Validates that a string contains only ASCII characters</li>
    <li><a href="/directives/string/email">Email</a> - Validates that a string is a valid email format</li>
    <li><a href="/directives/string/json">JSON</a> - Validates that a string is valid JSON</li>
    <li><a href="/directives/string/lowercase">Lowercase</a> - Validates that a string is entirely lowercase</li>
    <li><a href="/directives/string/not-blank">Not Blank</a> - Validates that a string is not empty or whitespace-only</li>
    <li><a href="/directives/string/not-regex">Not Regex</a> - Validates that a string doesn't match a pattern</li>
    <li><a href="/directives/string/regex">Regex</a> - Validates that a string matches a pattern</li>
    <li><a href="/directives/string/uppercase">Uppercase</a> - Validates that a string is entirely uppercase</li>
    <li><a href="/directives/string/url">URL</a> - Validates that a string is a valid URL</li>
</ul>